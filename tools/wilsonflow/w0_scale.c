/* Linked list that accomodate one flow.
 * There is one entry per flow time. */

struct list_flow {
  double t;
  double et;
  double ex;
  int *nentry; /* shared integer object that knows the length of the list */
  struct list_flow *next_entry;
};

/* Linked list that accomodate one flow.
 * There is one entry per flow time. */

struct list_conf {
  struct list_flow *flow;
  int *nentry;/* shared integer object that knows the length of the list */
  struct list_conf *next_entry;
};

/* This points to our function of choice, that calculates relevant flow.
 * The use of the temporal, spatial or full wlow is implemented this way.
 * It can be flow_value_avg (default), flow_value_x or flow_value_t.
 */

double (*flow_value)(const struct list_flow *);

void *xmalloc(size_t s)/*{{{*/
{
  /* allocate-or-die function */
  void *t=malloc(s);
  if(!t) err(1,"malloc has failed for %lu bytes",(unsigned long)s);
  return t;
}/*}}}*/

struct list_flow *read_flow(FILE *F)/*{{{*/
{
  /* Reads in one flow data from the file.  There can be more
   * flow data concatenated in a single file. */
  /* The data is put into a linked list structure,
   * its head element is returned */
  char line[max_line_len];
  double t,et,ex;
  struct list_flow *first_entry=NULL,*this_entry=NULL;
  while(fgets(line,max_line_len,F)) {
    if(sscanf(line,Flow_line_format,&t,&et,&ex)==3) {
      if(first_entry==NULL)  {
	this_entry=first_entry=xmalloc(sizeof(*first_entry));
	first_entry->nentry=xmalloc(sizeof(int));
	first_entry->nentry[0]=0;
      } else {
	this_entry->next_entry=xmalloc(sizeof(*first_entry));
	this_entry->next_entry->nentry=this_entry->nentry;
	this_entry=this_entry->next_entry;
      }
      this_entry->t=t;
      this_entry->et=et;
      this_entry->ex=ex;
      this_entry->next_entry=NULL;
      this_entry->nentry[0]++;
    } else if(first_entry) break; 
    /* we have already read in a flow, here it ends */
  }
  /* last read non-flow line is lost. It is usually some footer line. */
  return first_entry;
}/*}}}*/

struct list_conf *read_conf(char *file_list[],int nfile)/*{{{*/
{
  /* this function loops over a list of files,
   * with one or more flow data in each. 
   * A linked list is prodeced, with one entry per flow.
   * The pointer to the head entry is returned. */
  struct list_conf *first_conf=NULL,*this_conf=NULL;
  struct list_flow *this_flow;
  int i;
  for(i=0;i<nfile;i++) {
    FILE *F;
    F=fopen(file_list[i],"r");
    if(!F) {
      warnx("I could not read the input file %s",file_list[i]);
      continue;
    }
    while((this_flow=read_flow(F))) {
      struct list_conf *new_conf=xmalloc(sizeof(*first_conf));
      if(first_conf==NULL) {
	this_conf=first_conf=new_conf;
	first_conf->nentry=xmalloc(sizeof(int));
	first_conf->nentry[0]=0;
      } else {
	this_conf->next_entry=new_conf;
	this_conf=new_conf;
	this_conf->nentry=first_conf->nentry;
      }
      this_conf->nentry[0]++;
      this_conf->flow=this_flow;
      this_conf->next_entry=NULL;
    }
    fclose(F);
  }
  return first_conf;
}/*}}}*/

int find_max_flow_length(const struct list_conf *L)/*{{{*/
{
  /* Flow lengths are not necessarily uniform in the ensemble.
   * This function finds the longest one and returns its length.  */
  int max=0;
  while(L) {
    if(max<L->flow->nentry[0]) max=L->flow->nentry[0];
    L=L->next_entry;
  }
  return max;
}/*}}}*/

int count_configs_with_flow_at_least_that_long(const struct list_conf *L,int min)/*{{{*/
{
  /* Flow lengths are not necessarily uniform in the ensemble.
   * This function finds the number of flows that have at least 'min'
   * entries */
  int n=0;
  while(L) {
    if(min<=L->flow->nentry[0]) n++;
    L=L->next_entry;
  }
  return n;
}/*}}}*/

int find_ideal_flow_length(const struct list_conf *L)/*{{{*/
{
  /* Flow lengths are not necessarily uniform in the ensemble. 
   * Since we take a derivative of the flow, it is necessary to
   * have the same configurations for all relevant 't' values.
   * This is a heuristic algorithm, where we drop the very short
   * flow files (that are likely to be results of a truncated run),
   * and keep t as big as possible, so that about 90% of the
   * configurations are in.
   * */
  int nall,nconf;
  int max=find_max_flow_length(L);
  int try=max;
  if(L==NULL) return 0;
  nall=L->nentry[0];
  do {
    max=try;
    nconf=count_configs_with_flow_at_least_that_long(L,max);
    try-=5;
  } while(try>0 && nconf<nall*0.9);
  return max;
}/*}}}*/

/* The following functions will be called through the flow_value pointer {{{ */

double flow_value_avg(const struct list_flow *F)/*{{{*/
{
  return F->t*F->t*(F->et+F->ex);
}/*}}}*/

double flow_value_t(const struct list_flow *F)/*{{{*/
{
  return F->t*F->t*2*F->et;
}/*}}}*/

double flow_value_x(const struct list_flow *F)/*{{{*/
{
  return F->t*F->t*2*F->ex;
}/*}}}*/

/* flow_value functions }}} */

struct cspline * spline_from_flow(const struct list_conf *L)/*{{{*/
{
  /* This function returns a pointer to the first element of an array
   * of (Njack+1) cspline objects.
   * The first one is an interpolation of the full sample, the rest
   * correspond to the a jack-knife sample */
  int nconf;
  int j,n,f;
  struct cspline *S;
  double *F_array;
  double **JF,**NF;
  Nline=find_ideal_flow_length(L);
  nconf=count_configs_with_flow_at_least_that_long(L,Nline);
  if(nconf==0) errx(1,"No configuration");
  F_array=dalloc(Nline); /* a local copy of the flow */
  JF=jack_array_new(Nline);
  NF=jack_array_new(Nline);
  T_array=dalloc(Nline); /* global time array */
  for(n=0;L;L=L->next_entry) if(Nline<=L->flow->nentry[0]) {
    struct list_flow *F=L->flow;
    for(f=0;f<Nline && F;F=F->next_entry,f++) {
      jack_add(JF[f],flow_value(F),n,nconf);  /* data */
      jack_add(NF[f],1.0,n,nconf);            /* counter */
      if(n==0) T_array[f]=F->t;
      else if(T_array[f]!=F->t)
	errx(1,"Mismatch in the flow time");
    }
    n++;
  }
  /* data normalized: average within the jack-knife sample*/
  /* we do this for every flow time here */
  for(f=0;f<Nline;f++) jack_div(JF[f],JF[f],NF[f]); 
  jack_array_delete(NF);
  T_max=T_array[Nline-1];
  S=xmalloc(sizeof(*S)*(Njack+1));
  for(j=0;j<=Njack;j++) {
    for(f=0;f<Nline;f++) F_array[f]=JF[f][j];
    if(cspline_init(&S[j],T_array,F_array,Nline))
      errx(1,"spline initialization has failed with %d long flows",Nline);
  }
  free(F_array);
  jack_array_delete(JF);
  return S;
}/*}}}*/

double solve_w0(const struct cspline *S,double cut,double lo,double hi)/*{{{*/
{
  /* a very simple equation solver */
  /* the result must be bracketed between lo and hi */
  /* the resulting t is returned */
  const double tol=1e-6;
  if(hi<lo) return HUGE_VAL;
  while(hi-lo>tol) {
    double mid=(lo+hi)/2.;
    double y=mid*cspline_derive(S,mid);
    if(y<cut) lo=mid; else hi=mid;
  }
  return (lo+hi)/2.;
}/*}}}*/

double get_the_scale(struct cspline *S,double cut)/*{{{*/
{
  double lo=0.5; /* below that the flow is very non-universal */
  double hi=T_max;
  double t,scale;
  t=solve_w0(S,cut,lo,hi);
  scale=sqrt(t);
  if(Opt_f) return Scale_in_fermi/scale;
  else return scale;
}/*}}}*/

void tell_the_scale(struct cspline *spline,double cut)/*{{{*/
{
  int j;
  double *scale=jack_new();
  for(j=0;j<=Njack;j++)
    scale[j]=get_the_scale(&spline[j],cut);
  if(jack_error(scale)==0.0) {
    if(!Opt_o[0])
      printf("Scale determination has failed. Check the flow using -o\n");
    else
      printf("Scale determination has failed. Check the flow.\n");
  } else {
    if(Opt_j) {
      jack_print(stdout,scale);
      putchar('\n');
    } else {
      printf("%g %g\n",scale[0],jack_error(scale));
    }
  }
  jack_delete(scale);
}/*}}}*/

double scale_in_fermi(double cut)/*{{{*/
{
  double s;
  if(cut==0.3) {
    s=0.1755;
  } else {
    errx(1,"I do not know the scale if the cut is not at 0.3");
  }
  return s;
}/*}}}*/

void write_flow(const char *name,struct cspline *spline)/*{{{*/
{
  /* The field strength and its derivatives are written out
   * in dimensionless combinations of derivatives and products by 't' */
  int l,j;
  double *flow=jack_new();
  double *dflow=jack_new();
  double *ddflow=jack_new();
  FILE *F=fopen(name,"w");
  if(!F) err(1,"Could not create the output file: %s",name);
  for(l=0;l<Nline;l++) {
    double t=T_array[l];
    for(j=0;j<=Njack;j++) {
      flow[j]=cspline_val(&spline[j],t);
      dflow[j]=t*cspline_derive(&spline[j],t);
      ddflow[j]=t*t*cspline_derive2(&spline[j],t);
    }
    fprintf(F,"%g %g %g %g %g %g %g\n",t,
	flow[0],jack_error(flow),
	dflow[0],jack_error(dflow),
	ddflow[0],jack_error(ddflow)
	);
  }
  fclose(F);
  jack_delete(flow);
  jack_delete(ddflow);
  jack_delete(dflow);
}/*}}}*/

void flow_format(const char *label)/*{{{*/
{
  /* This function initializes the parser of the flow files.
   * We simply set the format string that will be used
   * later in an sscanf call from read_flow()
   */
  snprintf(Flow_line_format,sizeof(Flow_line_format),
      "%s %%lf %%lf %%lf",label);
}/*}}}*/

void options(int argc,char **argv)/*{{{*/
{
  int o;
  flow_value=flow_value_avg;
  while((o=getopt(argc,argv,"hc:o:xtj:fs:"))!=-1) switch (o) {
    case 'h': help(); exit(0); break;
    case 'c': Cut_value=atof(optarg); break;
    case 'o': strncpy(Opt_o,optarg,sizeof(Opt_o)-1); break;
    case 'x': flow_value=flow_value_x; break;
    case 't': flow_value=flow_value_t; break;
    case 'j': Njack=atoi(optarg); Opt_j=1; break;
    case 'f': Opt_f=1; break;
    case 's': flow_format(optarg);break;
    default: puts("You can ask for help: -h"); 
	     exit(EXIT_FAILURE);
  }
}/*}}}*/

int main(int argc,char **argv)/*{{{*/
{
  struct list_conf *L;
  struct cspline *S;
  options(argc,argv);
  L=read_conf(argv+optind,argc-optind);
  S=spline_from_flow(L);
  if(Opt_f) Scale_in_fermi=scale_in_fermi(Cut_value);
  tell_the_scale(S,Cut_value);
  if(Opt_o[0]) write_flow(Opt_o,S);
  return 0;
}/*}}}*/
