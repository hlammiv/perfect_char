def equal(A, B):
    return norm(A-B) < 1e-5

def conclass_id(M):
    l=999
    for i in range(len(conclass)):
        if equal(conclass[i],M):
            l=i
    if l==999:
        print("no conjugacy class found")
    return l

def s108elm30(p,q,r,s):
    g=w**p*matrix_power(c,q)*matrix_power(e,r)*matrix_power(v,s)
    return g

def s108elm31(p,q,r,s):
    g=w**p*matrix_power(c,q)*matrix_power(e,r)*matrix_power(1j*v,s)
    return g

def s108elm40(p,q,r,s):
    g=matrix_power(c4,q)*matrix_power(e4,r)*matrix_power(v4,s)
    return g

