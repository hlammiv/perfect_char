/* This block of code is a general purpose cubic spline calculator 
 * Not all are relevant for the w0 scale calculator, but it is 
 * perhaps better not to disintegrate the package.
 * */

/* The basic interface:
 *
 * struct cspline my_spline;        you need such kind of a variable,
 *                                  and pass its address &my_spline to
 *                                  the routines below.
 *
 * int cspline_init(struct cspline *C,double *xin,double *yin,int n);
 *      It initializes the spline (allocate more memory, calculate
 *      the coefficients, etc).
 *      xin is the X data, yin is the array of Y data, with n numbers each.
 *      The input array xin must be sorted. (In either way.)
 *      It returns 0 on success.
 *
 * void cspline_destroy(struct cspline *C);
 *      free the allocated memory. Two consecutive destroys, are safe,
 *      destroying an unititialized object is not.
 *
 * int cspline_is_initialized(struct cspline *C);
 *      It may be called only if the spline has already been initialized,
 *      but possibly later destroyed.
 *      It returns 1 if C is initialized, 0 if not.
 *
 * The following functions must be called with an initialized spline.
 *
 * double cspline_val(const struct cspline *C,double x);
 *      The spline interpolation at x.
 *      You get 'inf' when x is out of range.
 *
 * double cspline_derive(const struct cspline *C,double x);
 *      The derivative of the spline interpolation at x.
 *      You get 'inf' when x is out of range.
 *
 * double cspline_derive2(const struct cspline *C,double x);
 *      The 2nd derivative of the spline interpolation at x.
 *      You get 'inf' when x is out of range.
 *
 * double cspline_integrate(const struct cspline *C,double x)
 *      The integral of the interpolation from the smallest xin value to x.
 *      Again, you get 'inf' when x is out of range.
 *
 * double cspline_integrate_r(const struct cspline *C,double x)
 *      The integral of the interpolation from x to the largest xin value.
 *      Again, you get 'inf' when x is out of range.
 *
 * double cspline_integrate_full(const struct cspline *C)
 *      The integral of the interpolation from the smallest
 *      to the largest xin value.
 *
 */
#include <stdlib.h>
#include <math.h>
#include <float.h>
struct cspline {/*{{{*/
  const double *x; /* x data points, sorted */
  double (*c)[4]; /* coefficients */
  int n; /* number of data points */
};/*}}}*/

double cspline_integrate_full(const struct cspline *C)/*{{{*/
  /* integrate the full range of the spline */
{
#define primfn(l,x) (C->c[l][0]*x+C->c[l][1]*x*x/2.+C->c[l][2]*x*x*x/3.+C->c[l][3]*x*x*x*x/4.)
#define integral(l) (primfn(l,b[l+1])-primfn(l,b[l]))
  int i,r=C->n-1;
  const double *b=C->x;
  double s=0;
  for(i=0;i<r;i++) s+=integral(i);
  return s;
#undef integral
#undef primfn
}/*}}}*/

double cspline_integrate(const struct cspline *C,double x)/*{{{*/
  /* integrate from initial point to x */
{
#define in_range(x,left,right) ((b[left]-x)*(x-b[right])>-DBL_EPSILON) 
#define primfn(l,x) (C->c[l][0]*x+C->c[l][1]*x*x/2.+C->c[l][2]*x*x*x/3.+C->c[l][3]*x*x*x*x/4.)
#define integral(l) (primfn(l,b[l+1])-primfn(l,b[l]))
  int i,r=C->n-1;
  const double *b=C->x;
  double s=0;
  if(!in_range(x,0,r)) return HUGE_VAL;
  for(i=0;i<r;i++) {
    if(in_range(x,i,i+1)) {
      s+=primfn(i,x)-primfn(i,b[i]);
      break;
    } else {
      s+=integral(i);
    }
  }
  return s;
#undef integral
#undef primfn
#undef in_range
}/*}}}*/

double cspline_integrate_r(const struct cspline *C,double x)/*{{{*/
  /* integrate from x to the final point */
{
#define in_range(x,left,right) ((b[left]-x)*(x-b[right])>-DBL_EPSILON) 
#define primfn(l,x) (C->c[l][0]*x+C->c[l][1]*x*x/2.+C->c[l][2]*x*x*x/3.+C->c[l][3]*x*x*x*x/4.)
#define integral(l) (primfn(l,b[l+1])-primfn(l,b[l]))
  int i,r=C->n-1;
  const double *b=C->x;
  double s=0;
  if(!in_range(x,0,r)) return HUGE_VAL;
  for(i=r-1;i>=0;i--) {
    if(in_range(x,i,i+1)) {
      s+=primfn(i,b[i+1])-primfn(i,x);
      break;
    } else {
      s+=integral(i);
    }
  }
  return s;
#undef integral
#undef primfn
#undef in_range
}/*}}}*/

double cspline_derive(const struct cspline *C,double x)/*{{{*/
{
#define in_range(x,left,right) ((b[left]-x)*(x-b[right])>-DBL_EPSILON) 
  int m,l=0,r=C->n-1;
  const double *b=C->x;
  /* search interpolating interval */
  if(!in_range(x,l,r)) return HUGE_VAL;
  while(l+1<r) {
    m=(l+r)/2;
    if(in_range(x,m,r)) l=m; else r=m;
  }
  /* use polynomial */
  return C->c[l][1]+C->c[l][2]*x*2.+C->c[l][3]*x*x*3.;
#undef in_range
}/*}}}*/

double cspline_derive2(const struct cspline *C,double x)/*{{{*/
{
#define in_range(x,left,right) ((b[left]-x)*(x-b[right])>-DBL_EPSILON) 
  int m,l=0,r=C->n-1;
  const double *b=C->x;
  /* search interpolating interval */
  if(!in_range(x,l,r)) return HUGE_VAL;
  while(l+1<r) {
    m=(l+r)/2;
    if(in_range(x,m,r)) l=m; else r=m;
  }
  /* use polynomial */
  return C->c[l][2]*2.+C->c[l][3]*x*6.;
#undef in_range
}/*}}}*/

double cspline_val(const struct cspline *C,double x)/*{{{*/
{
#define in_range(x,left,right) ((b[left]-x)*(x-b[right])>-DBL_EPSILON) 
  int m,l=0,r=C->n-1;
  const double *b=C->x;
  /* search interpolating interval */
  if(!in_range(x,l,r)) return HUGE_VAL;
  while(l+1<r) {
    m=(l+r)/2;
    if(in_range(x,m,r)) l=m; else r=m;
  }
  /* use polynomial */
  return C->c[l][0]+C->c[l][1]*x+C->c[l][2]*x*x+C->c[l][3]*x*x*x;
#undef in_range
}/*}}}*/

int cspline_init(struct cspline *C,double *xin,double *yin,int n)/*{{{*/
  /* input array must be sorted wrt x in either way */
{
#define nalloc(u) (u=malloc(sizeof(*u)*n))
  double *x,*y,*z,*h,*d,*b;
  double (*c)[4];
  int i;
  x=y=z=h=d=b=NULL;c=NULL;
  if(n<2) return -1;
  if(!(nalloc(x)&&nalloc(y)&&nalloc(z)&&nalloc(h)&&nalloc(d)&&nalloc(b)&&nalloc(c))) {
    free(x);free(y);free(z);free(h);free(b);free(d);free(c);
    return -1;
  }
  if(xin[0]<xin[n-1]) for(i=0;i<n;i++) { x[i]=xin[i]; y[i]=yin[i]; }
  else for(i=0;i<n;i++) { x[i]=xin[n-i-1]; y[i]=yin[n-i-1]; }
  /* building the tridiagonal system: i=1..n-2  */
  for(i=0,h[n-1]=0;i<n-1;i++) h[i]=x[i+1]-x[i]; /* subdiag */
  for(i=1;i<n-1;i++) { 
    d[i]=2.*(x[i+1]-x[i-1]);                  /* diagonal */
    b[i]=6.*((y[i+1]-y[i])/h[i]-(y[i]-y[i-1])/h[i-1]); /* right hand side */
  }
  /* Gauss elimination */
  for(i=2;i<n-1;i++) { d[i]-=h[i-1]*h[i-1]/d[i-1]; b[i]-=h[i-1]*b[i-1]/d[i-1]; }
  /* back-substitution */
  for(i=n-2,z[0]=z[n-1]=0;i>0;i--) z[i]=(b[i]-h[i]*z[i+1])/d[i];
  /* building the 3rd order polynom's coefficients */
  for(i=0;i<n-1;i++) {
    c[i][0]=(x[i+1]*y[i]-x[i]*y[i+1])/h[i]+h[i]/6.*(z[i+1]*x[i]-z[i]*x[i+1])
      +(z[i]*x[i+1]*x[i+1]*x[i+1]-z[i+1]*x[i]*x[i]*x[i])/(6.*h[i]);
    c[i][1]=(z[i+1]*x[i]*x[i]-z[i]*x[i+1]*x[i+1])/(2.*h[i])+
      (y[i+1]-y[i])/h[i]-h[i]*(z[i+1]-z[i])/6.;
    c[i][2]=(z[i]*x[i+1]-z[i+1]*x[i])/(2.*h[i]);
    c[i][3]=(z[i+1]-z[i])/(6.*h[i]);
  }
  C->c=c; C->x=x; C->n=n;
  free(y);free(z);free(h);free(b);free(d);
  return 0;
#undef nalloc
}/*}}}*/

int cspline_is_initialized(struct cspline *C)/*{{{*/
{
  return C->c!=NULL;
}/*}}}*/

void cspline_destroy(struct cspline *C)/*{{{*/
{
  free((void*)C->x);
  free(C->c); C->c=0;
  C->n=0;
}/*}}}*/

