/* jackknife {{{ */

int Njack=20;

static double *dalloc(size_t ndouble)
{
  double *x=malloc(sizeof(double)*ndouble);
  if(x==NULL) err(1,"Crashed when allocating %u doubles",(unsigned)ndouble);
  return x;
}

static double **dalloc2(size_t x,size_t y)
  /*   array up to [x-1][y-1] */
{
  double **ptr,*data;
  size_t i;
  ptr=malloc(sizeof(double)*x);
  if(ptr==NULL) err(1,"Crashed when allocating %u pointers",(unsigned)x);
  data=malloc(sizeof(double)*x*y);
  if(data==NULL) err(1,"Crashed when allocating %u doubles",(unsigned)(x*y));
  for(i=0;i<x;i++) ptr[i]=&data[i*y];
  return ptr;
}

static void dfree2(double **ptr)
{
  free(ptr[0]);
  free(ptr);
}

void jack_reset(double *J)
{
  int j;
  for(j=0;j<=Njack;j++) J[j]=0.;
}

double * jack_new()
{
  /* [0]: full sample, rest: jack-knife */
  double *J=dalloc(Njack+1);
  jack_reset(J);
  return J;
}

void jack_delete(double *J)
{
  free(J);
}

void jack_array_delete(double **JA)
{
  dfree2(JA);
}

double **jack_array_new(int n_observable)
{
  double **JA=dalloc2(n_observable,Njack+1);
  int i;
  for(i=0;i<n_observable;i++) jack_reset(JA[i]);
  return JA;
}

void jack_add(double *J,double data,int configuration_index,int nconfiguration)
{
  int j,js=((configuration_index*Njack)/nconfiguration)%Njack+1;
  for(j=0;j<=Njack;j++) if(j!=js) J[j]+=data;
}

void jack_div(double *R,const double *J,const double *N)
{
  int j;
  for(j=0;j<=Njack;j++) R[j]=J[j]/N[j];
}

double jack_error(double *J)
{
  int j;
  double devsq=0;
  for(j=1;j<=Njack;j++) devsq+=pow(J[j]-J[0],2.);
  return sqrt(devsq*(Njack-1)/Njack);
}

void jack_print(FILE *F,double *J)
{
  int j;
  fprintf(F,"%g %g",J[0],jack_error(J));
  for(j=1;j<=Njack;j++) fprintf(F," %g",J[j]);
}

